            <div id="intro">
                <p>Le notaire est<strong> un juriste</strong> investi d’une <strong>mission d’autorité publique</strong> qui prépare des contrats sous la <strong>forme authentique</strong> pour le compte de ses clients. Il exerce ses fonctions dans un cadre libéral.</p>
            </div>
            <div class="paragraph">
                <h1> Le notaire, un officier public </h1>
                <p>Le notaire est un officier public, intervenant dans l’ensemble des domaines du droit&nbsp;: famille, immobilier, patrimoine, entreprises, rural, collectivités locales...<br>
                    Agissant pour le compte de l’Etat, nommé par le ministre de la justice, il confère aux actes qu’il rédige un gage de sérieux et d’authenticité. Cela signifie qu’il possède de véritables prérogatives de puissance publique, qu’il reçoit de l’Etat.<br>
                    Selon les termes de l’article&nbsp;1er de l’ordonnance du 2&nbsp;novembre&nbsp;1945 relative au statut du notariat&nbsp;: «&nbsp;Les notaires sont les officiers publics établis pour recevoir tous les actes et contrats auxquels les parties doivent ou veulent faire donner le caractère d’authenticité attaché aux actes de l’autorité publique&nbsp;».</p>
            </div>
            <div class="paragraph">
                <h1> Le notaire, un professionnel de l'authentification des actes </h1>
                <p>Il a le pouvoir d’authentifier les actes en apposant son sceau et sa propre signature. Il constate ainsi officiellement la volonté exprimée par les personnes qui les signent et s’engage personnellement sur le contenu et sur la date de l’acte. Cet acte s’impose alors avec la même force qu’un jugement définitif. A ce titre, le notaire est le magistrat de l’amiable, acteur d’une justice amiable.</p>
            </div>
            <div class="paragraph">
                <h1> Le notaire, un professionnel libéral </h1>
                <p>Bien qu’investi de l’autorité publique, le notaire exerce ses fonctions dans un cadre libéral, assurant ainsi une forme moderne de service public sans coût pour l’Etat, puisqu’il assume la responsabilité économique de son étude. C’est un professionnel libéral, rémunéré par ses clients (et non par les contribuables) selon un tarif fixé par l’Etat pour les services qu’il rend.<br>Consulter la page dédiée au tarif du notaire.</p>
            </div>
            <div class="paragraph">
                <h1> Le notaire, un professionnel présent sur tout le territoire </h1>
                <p>Implanté sur tout le territoire en vertu d’une répartition arrêtée par le ministre de la justice en fonction des besoins de la population, il assure un service public juridique de proximité. En effet, les offices notariaux ne sont pas soumis à un «&nbsp;numerus clausus&nbsp;» particulier (ce qui équivaudrait à une limitation du nombre des notaires) mais sont soumis à une implantation encadrée des offices sur tout le territoire pour répondre aux besoins de la population.</p>
                <p>L'implantation des offices sur le territoire fait l’objet d’une adaptation permanente sous le contrôle de la chancellerie. Elle obéit à trois principes&nbsp;:</p>
                <ul>
                    <li>maintenir un service public juridique de proximité,</li>
                    <li>tenir compte des évolutions géographiques et démographiques,</li>
                    <li>veiller aux conditions économiques d’exercice de la profession afin d’assurer un service de qualité.</li>
                </ul>
            </div>
